package aulas.aula05.ex02;

public class Pessoa {

	private String nome;
	private int idade;
	private String cpf;
	private String logradouro;
	private int numero;
	private String bairro;
	private String cidade;
	private String estado;
	private int cep;
	
	public Pessoa(String nome, int idade, String cpf, String logradouro, int numero, String bairro, String cidade,
			String estado, int cep) {
		
		this.nome = nome;
		this.idade = idade;
		this.cpf = cpf;
		this.logradouro = logradouro;
		this.numero = numero;
		this.bairro = bairro;
		this.cidade = cidade;
		this.estado = estado;
		this.cep = cep;
	}
	
	
}
